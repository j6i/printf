#include "libft.h"
#include <string.h>

int		ft_atoi(const char *s1)
{
	long	n;
	int		neg;

	n = 0;
	neg = 0;
	while (*s1 != '\0' && ((*s1 < 14 && *s1 > 8) || *s1 == 32))
		s1++;
	if (*s1 == '-')
	{
		neg = 1;
		s1++;
	}
	else if (*s1 == '+')
		s1++;
	while (*s1 > 47 && *s1 < 58 && *s1 != '\0')
	{
		n = (10 * n) - (*s1 - '0');
		s1++;
		if (n > 0)
			return (neg ? (int)0 : (int)-1);
	}
	return (neg ? (int)n : (int)-n);
}
