#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char *s;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n == 2147483647)
		write(fd, "2147483647", 10);
	else if (n != -2147483648 && n != 2147483647)
	{
		s = ft_itoa(n);
		while (*s != '\0')
			write(fd, &*s++, 1);
	}
}
